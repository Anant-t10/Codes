public class Main {
    // This is not the standard entry point for the program
    public static void main(int value) {
        System.out.println("This is an overridden main method.");
        System.out.println("Value: " + value);
    }

    // The standard entry point for the program
    public static void main(String[] args) {
        System.out.println("This is the standard main method.");
    }
}
